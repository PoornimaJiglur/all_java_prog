package switchstmt;

public class E2 {

	public static void main(String[] args) {
		
		char choice='a';
		switch(choice)
		{
		case 'a':System.out.println("Antartica");
		break;
		case 'b':System.out.println("Bali");
		break;
		case 'c':System.out.println("Canada");
		break;
		case 'd':System.out.println("Darjeeling");
		break;
		case 'e':System.out.println("Europe");
		break;
		default:System.out.println("Give proper i/p");
		}

	}

}
