package noargmethods;

public class P1 {

	public static void evenOdd()
	{
		// even or odd using methods
				int i=8;
								
				if(i%2==0)
				{
					System.out.println(i +" is even");
				}
				else {
					System.out.println(i +" is odd");
				}
				
				
	}
	
	public static void main(String[] args) {
		
		evenOdd();
	}

}
