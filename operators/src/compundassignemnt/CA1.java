package compundassignemnt;

public class CA1 {

	public static void main(String[] args) {
	int a=10;
	a+=10; //a=a+10. 10+10
	System.out.println(a);  //20
	//-=
	
	int b=20;
	b-=10;
	System.out.println(b);   //10
	
	//*=
	int c=20;
	c*=10;
	System.out.println(c);   //200
	
	// /=
	int d=20;
	d/=10;
	System.out.println(d);   //2
	//%=
	int e=20;
	e%=10;
	System.out.println(e);  //0
	}

}
