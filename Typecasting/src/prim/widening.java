package prim;

public class widening {

	public static void main(String[] args) {
		// byte --- s,i,l,f,d
		
		byte b=4;
		short s=b;
		System.out.println(s);
		int i=s;
		System.out.println(i);
		long l=i;
		System.out.println(l);
		float f=l;
		System.out.println(f);
		double d=f;
		System.out.println(d);
			
			
	}

}
