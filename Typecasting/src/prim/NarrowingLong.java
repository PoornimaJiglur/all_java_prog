package prim;

public class NarrowingLong {

	public static void main(String[] args) {
	//long to int
		long l=34232323423l;
		int i=(int)l;
		System.out.println(i);
		
		
   //long to short
	short s=(short)l;
	System.out.println(s);
		
		
   // long to byte
		byte b=(byte)l;
		System.out.println(b);

	}

}
