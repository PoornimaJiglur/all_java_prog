package prim;

public class NarrowingFloat {

	public static void main(String[] args) {
			//float to long
		float f=45.34f;
		long l=(long)f;	
		System.out.println(l);
		
		
		//float to int
		int i=(int)f;
		System.out.println(i);
		
		
		
		// float to short
		short s=(short)f;
		System.out.println(f);
		
		
		//float to byte
		
		byte b=(byte)f;
		System.out.println(b);

	}

}
