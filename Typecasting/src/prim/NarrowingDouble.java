package prim;

public class NarrowingDouble {
	public static void main(String[] args)
	{
		// double to float
				double d= 34.2;
				float f=(float) d;
				System.out.println(f);
				//double to long
				double d1=54.2;
						long l=(long) d1;
				
				System.out.println(l);
				//double to int
				double d2=12.2;
				int i=(int) d2;
						
				System.out.println(i);
				//double to short
						double d3=122.2;
						short s=(short) d3;
								
						System.out.println(s);
						System.out.println(i);
						//double to byte
								double d4=2.2;
								byte b=(byte) d4;
										
								System.out.println(b);
	}

}
