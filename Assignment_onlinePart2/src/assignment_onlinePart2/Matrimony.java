package assignment_onlinePart2;

public class Matrimony {
	private String name;
	private int age;
	private String gender;
	public Matrimony(String name, int age, String gender) {
		
		this.name = name;
		this.age = age;
		this.gender = gender;
	}
	public String getName()
	{
		return name;
	}
	public int getAge()
	{
		return age;
	}
	public String getgender()
	{
		return gender;
	}

}
